package com.example.resatravels;

public class Heesha_Vehicle {

    private String vehicle_type;
    private String vehicle_make;
    private String vehicle_plate;
    private String vehicle_location;
    private String vehicle_passenger;
    private String vehicle_price;
    private String vehicle_desc;
    private String vehicle_owner;
    private String vehicle_mobile;
    private String image;
    private String date1;
    private String time1;

    public Heesha_Vehicle() {
    }

    public Heesha_Vehicle(String vehicle_type, String vehicle_make, String vehicle_plate, String vehicle_location, String vehicle_passenger, String vehicle_price, String vehicle_desc, String vehicle_owner, String vehicle_mobile, String image, String date1, String time1) {
        this.vehicle_type = vehicle_type;
        this.vehicle_make = vehicle_make;
        this.vehicle_plate = vehicle_plate;
        this.vehicle_location = vehicle_location;
        this.vehicle_passenger = vehicle_passenger;
        this.vehicle_price = vehicle_price;
        this.vehicle_desc = vehicle_desc;
        this.vehicle_owner = vehicle_owner;
        this.vehicle_mobile = vehicle_mobile;
        this.image = image;
        this.date1 = date1;
        this.time1 = time1;
    }

    public String getVehicle_type() {
        return vehicle_type;
    }

    public void setVehicle_type(String vehicle_type) {
        this.vehicle_type = vehicle_type;
    }

    public String getVehicle_make() {
        return vehicle_make;
    }

    public void setVehicle_make(String vehicle_make) {
        this.vehicle_make = vehicle_make;
    }

    public String getVehicle_plate() {
        return vehicle_plate;
    }

    public void setVehicle_plate(String vehicle_plate) {
        this.vehicle_plate = vehicle_plate;
    }

    public String getVehicle_location() {
        return vehicle_location;
    }

    public void setVehicle_location(String vehicle_location) {
        this.vehicle_location = vehicle_location;
    }

    public String getVehicle_passenger() {
        return vehicle_passenger;
    }

    public void setVehicle_passenger(String vehicle_passenger) {
        this.vehicle_passenger = vehicle_passenger;
    }

    public String getVehicle_price() {
        return vehicle_price;
    }

    public void setVehicle_price(String vehicle_price) {
        this.vehicle_price = vehicle_price;
    }

    public String getVehicle_desc() {
        return vehicle_desc;
    }

    public void setVehicle_desc(String vehicle_desc) {
        this.vehicle_desc = vehicle_desc;
    }

    public String getVehicle_owner() {
        return vehicle_owner;
    }

    public void setVehicle_owner(String vehicle_owner) {
        this.vehicle_owner = vehicle_owner;
    }

    public String getVehicle_mobile() {
        return vehicle_mobile;
    }

    public void setVehicle_mobile(String vehicle_mobile) {
        this.vehicle_mobile = vehicle_mobile;
    }

    public String getDate1() {
        return date1;
    }

    public void setDate1(String date1) {
        this.date1 = date1;
    }

    public String getTime1() {
        return time1;
    }

    public void setTime1(String time1) {
        this.time1 = time1;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
