package com.example.resatravels;

public class hotel_facilities {
    public String foodsAndDrinks;
    public String outDoor;
    public String CleaningServices;
    public String SafetyFeatures;
    public String HealthAndWellnessFacilities;
    public String Internet;
    public String Parking;

    public hotel_facilities() {
    }

    public String getFoodsAndDrinks() {
        return foodsAndDrinks;
    }

    public void setFoodsAndDrinks(String foodsAndDrinks) {
        this.foodsAndDrinks = foodsAndDrinks;
    }

    public String getOutDoor() {
        return outDoor;
    }

    public void setOutDoor(String outDoor) {
        this.outDoor = outDoor;
    }

    public String getCleaningServices() {
        return CleaningServices;
    }

    public void setCleaningServices(String cleaningServices) {
        CleaningServices = cleaningServices;
    }

    public String getSafetyFeatures() {
        return SafetyFeatures;
    }

    public void setSafetyFeatures(String safetyFeatures) {
        SafetyFeatures = safetyFeatures;
    }

    public String getHealthAndWellnessFacilities() {
        return HealthAndWellnessFacilities;
    }

    public void setHealthAndWellnessFacilities(String healthAndWellnessFacilities) {
        HealthAndWellnessFacilities = healthAndWellnessFacilities;
    }

    public String getInternet() {
        return Internet;
    }

    public void setInternet(String internet) {
        Internet = internet;
    }

    public String getParking() {
        return Parking;
    }

    public void setParking(String parking) {
        Parking = parking;
    }
}
